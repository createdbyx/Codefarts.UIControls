namespace Codefarts.UIControls
{
    /// <summary>
    /// Provides a enum describing a mouse event.
    /// </summary>
    public enum MouseEventType
    {
        /// <summary>
        /// Mouse enter event.
        /// </summary>
        MouseEnter,

        /// <summary>
        /// Mouse leave event.
        /// </summary>
        MouseLeave,

        /// <summary>
        /// Mouse move event.
        /// </summary>
        MouseMove,

        /// <summary>
        /// Mouse down event.
        /// </summary>
        MouseDown,

        /// <summary>
        /// Mouse up event.
        /// </summary>
        MouseUp
    }
}