namespace Codefarts.UIControls
{
    /// <summary>
    /// Specifies the direction in which an <see cref="Expander" /> control opens. 
    /// </summary>
    public enum ExpandDirection
    {
        /// <summary>
        /// The <see cref="Expander" /> control opens down.
        /// </summary>
        Down,

        /// <summary>
        /// The <see cref="Expander" /> control opens up.
        /// </summary>
        Up,

        /// <summary>
        /// The <see cref="Expander" /> control opens left.
        /// </summary>
        Left,

        /// <summary>
        /// The <see cref="Expander" /> control opens right. 
        /// </summary>
        Right
    }
}